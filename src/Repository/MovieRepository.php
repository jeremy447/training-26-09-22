namespace App\Repository;

use App\Entity\Genre;
use App\Entity\Movie;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\ORM\Query\Expr\Join;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Movie>
 *
 * @method Movie|null find($id, $lockMode = null, $lockVersion = null)
 * @method Movie|null findOneBy(array $criteria, array $orderBy = null)
 * @method Movie[]    findAll()
 * @method Movie[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class MovieRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Movie::class);
    }

    public function add(Movie $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Movie $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function fetch(string $title)
    {
        $qb = $this->createQueryBuilder('m');

        return $qb->select('partial m.{id,title}, partial g.{name}')
            ->innerJoin(Genre::class, 'g', Join::WITH, '')
            ->andWhere($qb->expr()->orX(
                $qb->expr()->like('m.title', $qb->expr()->literal('%:title%')),
                $qb->expr()->andX(
                    $qb->expr()->lte('m.releasedAt', new \DateTimeImmutable('1980')),
                    $qb->expr()->eq('', '') //...
                )
            ))
            ->setParameter('title', $title)
            ->getQuery()
            ->getResult();
    }

